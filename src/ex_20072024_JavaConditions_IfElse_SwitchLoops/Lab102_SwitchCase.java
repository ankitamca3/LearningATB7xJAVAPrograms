package ex_20072024_JavaConditions_IfElse_SwitchLoops;

public class Lab102_SwitchCase {
    public static void main(String[] args) {
        int a = 11;
        switch (-1){
            default:
                System.out.println("Default");
                break;
            case -1:
                System.out.println("10");
                break;
            case 9:
                System.out.println("10");
                break;
        }
    }
}
